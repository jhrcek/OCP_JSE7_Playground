package ocpjse7.processed;

import java.nio.file.FileSystems;

public class GetPathMatcher {

    public static void main(String[] args) {
        // 1) in glob, * represents 0+ characters (DOES NOT REFER TO PREVIOUS CHAR), . is LITERAL
        FileSystems.getDefault().getPathMatcher("glob:file*.txt");

        // 2) in regex * is quantifier representing 0+ occurences of PREVIOUS CHAR, . means ANY CHAR
        FileSystems.getDefault().getPathMatcher("regex:file*.txt");

        // Differences in glob vs. regex syntax
        System.out.println(convertGlobToRegEx("file*.txt"));

        // 3) It is MANDATORY to use syntax prefix (i.e. glob: or regex:, otherwise IllegalArgumentException is thrown
        // FileSystems.getDefault().getPathMatcher("file.txt");
    }

    static String convertGlobToRegEx(String line) {
        System.out.println("got line [" + line + "]");
        line = line.trim();
        int strLen = line.length();
        StringBuilder sb = new StringBuilder(strLen);
        // Remove beginning and ending * globs because they're useless
        if (line.startsWith("*")) {
            line = line.substring(1);
            strLen--;
        }
        if (line.endsWith("*")) {
            line = line.substring(0, strLen - 1);
            strLen--;
        }
        boolean escaping = false;
        int inCurlies = 0;
        for (char currentChar : line.toCharArray()) {
            switch (currentChar) {
                case '*':
                    if (escaping) {
                        sb.append("\\*");
                    } else {
                        sb.append(".*");
                    }
                    escaping = false;
                    break;
                case '?':
                    if (escaping) {
                        sb.append("\\?");
                    } else {
                        sb.append('.');
                    }
                    escaping = false;
                    break;
                case '.':
                case '(':
                case ')':
                case '+':
                case '|':
                case '^':
                case '$':
                case '@':
                case '%':
                    sb.append('\\');
                    sb.append(currentChar);
                    escaping = false;
                    break;
                case '\\':
                    if (escaping) {
                        sb.append("\\\\");
                        escaping = false;
                    } else {
                        escaping = true;
                    }
                    break;
                case '{':
                    if (escaping) {
                        sb.append("\\{");
                    } else {
                        sb.append('(');
                        inCurlies++;
                    }
                    escaping = false;
                    break;
                case '}':
                    if (inCurlies > 0 && !escaping) {
                        sb.append(')');
                        inCurlies--;
                    } else if (escaping) {
                        sb.append("\\}");
                    } else {
                        sb.append("}");
                    }
                    escaping = false;
                    break;
                case ',':
                    if (inCurlies > 0 && !escaping) {
                        sb.append('|');
                    } else if (escaping) {
                        sb.append("\\,");
                    } else {
                        sb.append(",");
                    }
                    break;
                default:
                    escaping = false;
                    sb.append(currentChar);
            }
        }
        return sb.toString();
    }
}
